class TwoLayerNetwork(torch.nn.Module):
    def __init__(self):
        super(TwoLayerNetwork, self).__init__()
        self.twolayerNetwork1 = torch.nn.Linear(784, 400)
        self.twolayerNetwork2 = torch.nn.Linear(400, 3)


    def forward(self, x):
        # x.shape = (n_batch, n_features)

        sigmoid = torch.nn.Sigmoid()
        u = sigmoid(self.twolayerNetwork1(x))
        outputs = self.twolayerNetwork2(u)
        return outputs

# load data from csv
