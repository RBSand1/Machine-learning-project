class OneLayerNetwork(torch.nn.Module):
    def __init__(self):
        super(OneLayerNetwork, self).__init__()
        self.oneLayerNetwork = torch.nn.Linear(784, 3)


    def forward(self, x):
        # x.shape = (n_batch, n_features)

        outputs = self.oneLayerNetwork(x)
        return outputs

